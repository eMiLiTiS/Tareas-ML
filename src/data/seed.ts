export function generateDailyTasks(date: string, templates: TaskTemplate[]): Task[] {
  const activeTemplates = templates.filter((template) => template.activa)

  return activeTemplates.map((template) => ({
    id: crypto.randomUUID(),
    titulo: template.titulo,
    descripcion: template.descripcion,
    categoria: template.categoria,
    area: template.area,
    tipoRespuesta: template.tipoRespuesta,
    valorRespuesta: null,
    estado: 'pendiente' as const,
    prioridad: template.prioridad,
    fecha: date,
    pacienteId: null,
    tratamiento: null,
  }))
}
